   <h1>Découverte paléontologique à Montpellier</h1>
  <div class="description">
  <p>
Une équipe d’archéologues et de paléontologues a mis au jour un fossile exceptionnel dans la périphérie nord de Montpellier.
Les premiers relevés laissent penser qu’il s’agirait des restes d’un reptile préhistorique de grande taille, possiblement un dinosaure herbivore.
</p>

<p>
Ce chantier, initialement prévu pour des fouilles classiques en contexte gallo-romain, a rapidement pris une ampleur scientifique considérable
après cette découverte inattendue. Des experts de plusieurs universités ont été dépêchés pour étudier la zone avec des techniques de fouille
adaptées aux spécimens paléontologiques.
</p>

<p>
La structure osseuse, bien conservée, permettra une datation précise ainsi qu'une reconstitution du squelette complet dans les mois à venir.
Ce type de découverte est extrêmement rare dans la région.
</p>
